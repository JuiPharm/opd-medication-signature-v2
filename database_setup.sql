CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- 1. Core tables
CREATE TABLE IF NOT EXISTS public.transactions (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    hn TEXT NOT NULL,
    staff_id TEXT NOT NULL,
    staff_name TEXT,
    receiver_type TEXT NOT NULL,
    signature_url TEXT,
    service_date DATE NOT NULL,
    device_id TEXT,
    status TEXT DEFAULT 'COMPLETED',
    drive_archive_url TEXT,
    is_duplicate_confirmed BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.staff_users (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    staff_id TEXT UNIQUE NOT NULL,
    name TEXT NOT NULL,
    pin TEXT,
    pin_hash TEXT,
    role TEXT DEFAULT 'STAFF',
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.staff_sessions (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    staff_user_id UUID NOT NULL REFERENCES public.staff_users(id) ON DELETE CASCADE,
    token_hash TEXT NOT NULL UNIQUE,
    expires_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE public.transactions ADD COLUMN IF NOT EXISTS is_duplicate_confirmed BOOLEAN DEFAULT FALSE;
ALTER TABLE public.staff_users ADD COLUMN IF NOT EXISTS pin_hash TEXT;
ALTER TABLE public.staff_users ALTER COLUMN pin DROP NOT NULL;

-- Seed/update the first administrator. Change this PIN after first login.
INSERT INTO public.staff_users (staff_id, name, pin, pin_hash, role)
VALUES ('admin', 'System Admin', NULL, crypt('123456', gen_salt('bf')), 'ADMIN')
ON CONFLICT (staff_id) DO UPDATE
SET name = EXCLUDED.name,
    pin = NULL,
    pin_hash = EXCLUDED.pin_hash,
    role = 'ADMIN';

-- Migrate any old plaintext PINs into hashes, then clear the plaintext.
UPDATE public.staff_users
SET pin_hash = crypt(pin, gen_salt('bf')),
    pin = NULL
WHERE pin_hash IS NULL
  AND pin IS NOT NULL
  AND pin <> '';

